    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {                                         
        if (inputPeso.getText().intern() == "") {
            JOptionPane.showMessageDialog(null, "Digite o seu peso", "Erro", JOptionPane.WARNING_MESSAGE);
        }
        else if (inputAltura.getText().intern() == "") {
            JOptionPane.showMessageDialog(null, "Digite a sua altura", "Erro", JOptionPane.WARNING_MESSAGE);
        }
        else if (Float.parseFloat(inputAltura.getText()) > 3) {
            JOptionPane.showMessageDialog(null, "Digite uma altura poss√≠vel para um ser humano. Informe a altura em metros\nExemplo: 1.80.", "Erro", JOptionPane.WARNING_MESSAGE);
        }
        
        else {
            float peso = Float.parseFloat(inputPeso.getText());
            float altura = Float.parseFloat(inputAltura.getText());
            float imc;
            
            imc = peso / (altura * altura);

            if (imc < 18) {
                textIMC.setText("Abaixo do peso ideal.");
            }
            else if (imc > 18 && imc < 24.99) {
                textIMC.setText("Peso ideal.");
            }
            else if (imc > 25) {
                textIMC.setText("Acima do peso ideal.");
            }
        }
    }                                        

